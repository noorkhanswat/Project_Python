# in python variable naming convention
# variable naming convention in python is snake_case
# snake_case is a naming convention where words are separated by underscores (_) and all letters are lowercase
# for example:
first_name = "John"
last_name = "Doe"
age = 30
city = "New York"
print("First Name:", first_name)
print("Last Name:", last_name)
print("Age:", age)
print("City:", city)    
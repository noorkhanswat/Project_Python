# what is the difference between list, tuple, set and dictionary in python
# lists are ordered, mutable, and allow duplicate elements. They are defined using square brackets []
my_list = [1, 2, 3, 4, 5]
print("List:", my_list)
# tuples are ordered, immutable, and allow duplicate elements. They are defined using parentheses ()
my_tuple = (1, 2, 3, 4, 5)
print("Tuple:", my_tuple)
# sets are unordered, mutable, and do not allow duplicate elements. They are defined using curly braces {}
my_set = {1, 2, 3, 4, 5}
print("Set:", my_set)
# dictionaries are unordered, mutable, and allow duplicate values but not duplicate keys. They are defined using curly braces {} with key-value pairs
my_dict = {"name": "Alice", "age    ": 30, "city": "New York"}
print("Dictionary:", my_dict)   